package Week2;

public class NumbersTwo {
    public static void main(String[] args){
        int first = 8;
        int second = 5;
        System.out.print(first + second);
        System.out.print(first * second);
        System.out.print(first / second);
        System.out.print(first % second);
        System.out.print(first - second);
    }
}
