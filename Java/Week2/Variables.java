package Week2;

public class Variables {
    public static void main(String[] args){
        boolean first = true;
        char letter = 'B';
        byte value = 120;
        short second = 32737;
        int third = 83;
        long fourth = 873019L;
        float distance = 4.69F;

        System.out.print("boolean" + first);
        System.out.print("char" + letter);
        System.out.print("byte" + value);
        System.out.print("short" + second);
        System.out.print("int" + third);
        System.out.print("long" + fourth);
        System.out.print("float" + distance);
    }

}
